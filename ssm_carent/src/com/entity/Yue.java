package com.entity;

public class Yue
{
    private int id;
    private String month;
    private Double jine;
    private int weihu;
    
    public int getId() {
        return this.id;
    }
    
    public void setId(final int id) {
        this.id = id;
    }
    
    public String getMonth() {
        return this.month;
    }
    
    public void setMonth(final String month) {
        this.month = month;
    }
    
    public Double getJine() {
        return this.jine;
    }
    
    public void setJine(final double d) {
        this.jine = d;
    }
    
    public int getWeihu() {
        return this.weihu;
    }
    
    public void setWeihu(final int weihu) {
        this.weihu = weihu;
    }
}
